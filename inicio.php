        <h4 class="page_title">
        <i class="fa fa-mobile-screen-button mx-1"></i>Curso de Especialização à Distância em  Desenvolvimento de Sistemas Web e Mobile (ESPWEBMOB)
        </h4>

        <br/>
        <hr/>
        <br/>

        <p>
            A <b>ESCOLA REGIONAL DE ENGENHARIA DE SOFTWARE (ERES)</b> é promovida anualmente pela Sociedade Brasileira de Computação (SBC). A quinta edição da escola, a ERES 2021, irá ocorrer TOTALMENTE ONLINE, no período de 01 a 03 de dezembro de 2021.
        </p>
        <br/>
        <p>
            A ERES tem por objetivo disseminar o conhecimento e boas práticas em Engenharia de Software (ES), do ponto de vista profissional e acadêmico. A ERES 2020 é um espaço regional para que possam ser apresentados os resultados de pesquisas de graduação e pós-graduação e relatos de experiência na indústria. Além disso, possibilitará um ambiente natural para a discussão de abordagens no ensino-aprendizagem na ES nas instituições de ensino superior da Região Sul do Brasil.
        </p>

        <br/>
        <p>
            A realização da ERES 2021 oferecerá aos seus participantes atualizações do estado-da-arte da pesquisa científica na área de engenharia de software por meio da apresentação de palestras ministradas por pesquisadores renomados. O evento também intenciona oferecer uma atualização tecnológica por meio de minicursos apresentados por pesquisadores ou profissionais experientes e qualificados. Ainda, oficinas e um painel serão realizados. Dessa forma, este evento irá possibilitar a divulgação de conceitos atuais na área, além de divulgar o que se está pesquisando em engenharia de software na região sul, no país e no mundo, inspirando assim o gosto pela pesquisa científica aos participantes.
        </p>
        
        <br/>
        <p>
            A Programação do ERES está sendo elaborada com palestras de profissionais da indústria, palestras da academia e submissões de artigos para serem apresentados em um fórum para graduação, um fórum de industria e um fórum para pós-graduação:
        </p>
        <br/>
        <ul>
            <li><p><b>Fórum para Graduação:</b> O Fórum para Graduação da ERES 2021 é um espaço destinado para apresentação dos trabalhos de pesquisa ou relatos de experiência em Engenharia de Software, desenvolvidos por acadêmicos de graduação, principalmente das IES catarinenses, gaúchas e paranaenses.</p><br/></li>

            <li><p><b>Fórum para Pós-Graduação:</b> O Fórum de Pós-Graduação da ERES 2021 é um espaço para apresentação de trabalhos de estudantes de pós-graduação, com o objetivo de incentivar a troca de experiências e divulgar pesquisas em andamento e/ou concluídas e resultados obtidos.. Além da clareza do trabalho, relevância do tema, e qualidade da apresentação, o Comitê Científico da ERES 2021 avaliará também as contribuições científicas do trabalho.</p><br/></li>

            <li><p><b>Trilha da Indústria:</b> A Trilha da Indústria de Software da ERES 2021 é um espaço para apresentação de trabalhos de profissionais do setor produtivo de T.I, ou de trabalhos acadêmicos resultantes de aplicações práticas na indústria de software e que apresentem real contribuição para o mercado de software. O objetivo é incentivar a troca de experiências entre profissionais da academia e indústria de software. Recomenda-se que ao menos um autor do artigo tenha sua afiliação associada a um dos setores da indústria de software.</p><br/></li>

        </ul>

